#include "libft.h"

int	ft_issort(int *arr, int len)
{
	int	i;
	int	j;

	i = 0;
	while (i < len)
	{
		j = i + 1;
		while (j < len)
		{
			if (arr[i] > arr[j])
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
