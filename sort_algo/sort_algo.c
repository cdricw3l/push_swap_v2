int is_sort(int *arr, int len)
{
    int i;
    int j;

    i = 0;
    while (i < len)
    {
        j = i + 1;
        while (j < len)
        {
            if(arr[i] > arr[j])
            {
                printf(CRED"array is not sorted\n"CRESET);
                return (0);
            }
            j++;
        }
        i++;
    }
    printf(CGREEN"array is sorted\n"CRESET);
    return(1);
}

